# Define a list of names
names = ["Alice", "Bob", "Charlie", "David", "Eve"]

# Adding a name
names.append("Draco")

# Sorting a name
names.sort()

# Printing the list
print(names)

# Removing a name
names.remove("Bob")

# Printing the list
print(names)

# Printing the length of the list
print(len(names))
